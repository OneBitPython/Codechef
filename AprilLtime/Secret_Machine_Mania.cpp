string bin(long n){
    long i;
    string val = "0";
    for (i = 1 << 30; i > 0; i = i / 2)
    {
      if((n & i) != 0)
      {
        val+="1";
      }
      else
      {
        val+="0";
      }
    }
    while(val.front()=='0'){
        val.erase(0,1);
    }
    return val;
}




void solve()
{
    int x, c;
    cin >> x >> c;
    map<int,int>f;
    int n= x;
    for(int i = 2;i<=sqrt(n);++i){
        if(n%i == 0){
            f[i] = 1;
            n/=i;
            while(n%i == 0){
                f[i]++;
                n/=i;
            }
        }
    }
    if(n>1)f[n]++;
    int ans = 1;
    for(auto itr = f.begin();itr!=f.end();++itr){
        int p = itr->first;
        int k = itr->second;
        int go = min(k%c, c-k%c);
        // dbg(p,k,c);
        ans*= (int)pow(p,go);
    }
    cout << ans << endl;
}   

